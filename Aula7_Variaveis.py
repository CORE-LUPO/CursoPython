# Variáveis são usadas para salvar algo na memória do computador.
# Elas recebem valores que podem ser usados depois no programa.

# Convenção PEP8: nomes de variáveis devem começar com letras minúsculas.
# Podem conter letras, números e underline (_), mas não devem começar com número.

# Atribuindo valores às variáveis
nome = 'Luiz'                      # string atribuída à variável 'nome'
idade = 17                         # número inteiro atribuído à variável 'idade'

# Operação lógica para verificar se a idade é maior ou igual a 18
maior_de_idade = idade >= 18      # 'maior_de_idade' será True se idade >= 18, senão False

# Exibindo informações com print()
print('Nome:', nome, 'Idade:', idade)          # imprime o nome e a idade
print('É maior?', maior_de_idade)              # imprime True ou False dependendo da idade
