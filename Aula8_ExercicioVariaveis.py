nome = 'Luiz Otávio'                 # variável do tipo string contendo o primeiro nome
sobrenome = 'Miranda'               # string com o sobrenome
idade = 18                          # variável do tipo inteiro com a idade da pessoa
ano_nascimento = 2022 - idade       # subtração: calcula o ano de nascimento (resultado: 2004)
maior_de_idade = idade >= 18        # verifica se idade é maior ou igual a 18 → retorna True ou False
altura_metros = 1.80                # variável do tipo float com a altura em metros

# Exibindo todas as informações formatadas:
print('Nome:', nome)                          # imprime: Nome: Luiz Otávio
print('Sobrenome:', sobrenome)                # imprime: Sobrenome: Miranda
print('Idade:', idade)                        # imprime: Idade: 18
print('Ano de nascimento:', ano_nascimento)   # imprime: Ano de nascimento: 2004
print('É maior de idade?', maior_de_idade)    # imprime: É maior de idade? True
print('Altura em metros:', altura_metros)     # imprime: Altura em metros: 1.8
