# Tipos int e float

# int -> número inteiro (sem parte decimal)
# Pode ser positivo, negativo ou zero
# Ex: 10, -3, 0

# float -> número de ponto flutuante (com parte decimal)
# Também pode ser positivo, negativo ou zero
# Ex: 1.5, -0.5, 0.0

# A função type() mostra o tipo de dado de um valor

print(type('Otávio'))  # string → <class 'str'>
print(type(0))         # inteiro → <class 'int'>
print(type(1.1), type(-1.1), type(0.0))  # float → <class 'float'> (todos)
